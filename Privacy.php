<p>This privacy policy will inform you how you can verify which of your personal information is collected by us through the Website &amp; Mobile App and how you can request that we delete, update or transfer it.</p>
<p>It is important that you read this privacy policy together with any other privacy policy or fair processing policy we may provide on specific occasions when we are collecting or processing personal data about you so that you are fully aware of how and why we are using your data. This privacy policy supplements other notices and privacy policies and is not intended to override them.</p>
<ol>
<li><strong>THE DATA WE COLLECT ABOUT YOU</strong></li>
</ol>
<p>Personal data, or personal information, means any information about an individual from which that person can be identified. It does not include data where the identity has been removed (anonymous data).</p>
<p>We may collect, use, store and transfer different kinds of personal data about you which we have grouped together as follows:</p>
<ul>
<li><strong>Identity Data</strong> includes first name, last name, username or similar identifier, title, date of birth and gender.</li>
<li><strong>Contact Data</strong> includes billing address, email address and telephone numbers.</li>
<li><strong>Transaction Data</strong> includes details about payments to and from you and other details of products and services you have purchased from us.</li>
<li><strong>Technical Data</strong> includes internet protocol (IP) address, your login data, browser type and version, time zone setting and location, browser plug-in types and versions, operating system and platform, and other technology on the devices you use to access this Website &amp; Mobile App.</li>
<li><strong>Profile Data </strong>includes your username and password, your interests, preferences, feedback and survey responses.</li>
<li><strong>Usage Data</strong> includes information about how you use our Website &amp; Mobile App, products and services.</li>
<li><strong>Marketing and Communications Data </strong>includes your preferences in receiving marketing from us and our third parties and your communication preferences.</li>
</ul>
<p>We also collect, use and share <strong>Aggregated Data</strong> such as statistical or demographic data for any purpose. Aggregated Data could be derived from your personal data but is not considered personal data in law as this data will <em>not</em> directly or indirectly reveal your identity. For example, we may aggregate your Usage Data to calculate the percentage of users accessing a specific Website &amp; Mobile App feature. However, if we combine or connect Aggregated Data with your personal data so that it can directly or indirectly identify you, we treat the combined data as personal data which will be used in accordance with this privacy policy.</p>
<p>We do not collect <strong>Special Categories of Personal Data</strong> about you (this includes details about your race or ethnicity, religious or philosophical beliefs, sex life, sexual orientation, political opinions, trade union membership, information about your health, and genetic and biometric data). Nor do we collect any information about criminal convictions and offences.</p>
