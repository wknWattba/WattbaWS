<li><strong>PURPOSE OF THIS PRIVACY POLICY</strong></li>
</ol>
<p>This privacy policy aims to give you information on how we collect and process your personal data through your use of this Website &amp; Mobile App, including any data you may provide through this Website &amp; Mobile App.</p>
