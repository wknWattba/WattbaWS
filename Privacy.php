<li><strong>IF YOU FAIL TO PROVIDE PERSONAL DATA</strong></li>
</ol>
<p>Where we need to collect personal data by law, or under the terms of a contract we have with you, and you fail to provide that data when requested, we may not be able to perform the contract we have or are trying to enter into with you (for example, to provide you with goods or services). In this case, we may have to cancel a product or service you have with us, but we will notify you if this is the case at the time.</p>
<ol>
<li><strong>HOW IS YOUR PERSONAL DATA COLLECTED?</strong></li>
</ol>
<p>We use different methods to collect data from and about you including through:</p>
<ol>
<li><strong>Direct interactions.</strong> You may give us your data by filling in forms or by corresponding with us by post, phone, email or otherwise. This includes personal data you provide when you:</li>
</ol>
<ul>
<li>apply for our services;</li>
<li>create an account on our Website &amp; Mobile App;</li>
<li>subscribe to our service or publications;</li>
<li>request marketing to be sent to you; or</li>
<li>give us feedback or contact us.</li>
</ul>
<ol>
<li><strong>Automated technologies or interactions.</strong> As you interact with our Website &amp; Mobile App, we will automatically collect Technical Data about your equipment, browsing actions and patterns. We collect this personal data by using cookies and other similar technologies. Please see our cookie policy for further details.</li>
</ol>
<ol>
<li><strong>Third parties or publicly available sources.</strong> We will receive personal data about you from various third parties and public sources as set out below:</li>
</ol>
<ul>
<li>Technical Data from analytics providers&nbsp;</li>
<li>Contact and Transaction Data from providers of technical, payment and delivery services.</li>
<li>Identity and Contact Data from data brokers or aggregators</li>
<li>Identity and Contact Data from publicly available sources&nbsp;</li>
</ul>
<ol>
<li><strong>HOW WE USE YOUR PERSONAL DATA</strong></li>
</ol>
<p>We will only use your personal data when the law allows us to. Most commonly, we will use your personal data in the following circumstances:</p>
<ul>
<li>Where we need to perform the contract, we are about to enter into or have entered into with you.</li>
<li>Where it is necessary for our legitimate interests (or those of a third party) and your interests and fundamental rights do not override those interests.</li>
<li>Where we need to comply with a legal obligation.&nbsp;</li>
</ul>
<p>Generally, we do not rely on consent as a legal basis for processing your personal data although we will get your consent before sending third party direct marketing communications to you via email or text message. You have the right to withdraw consent to marketing at any time by contacting us.</p>
